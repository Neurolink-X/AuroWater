/**
 * Concatenates ordered sql migrations into sql/ALL_MIGRATIONS_ORDERED.sql for Supabase SQL Editor.
 */
import { readFileSync, writeFileSync } from 'fs';
import { dirname, join } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const files = [
  '001_core_schema.sql',
  '002_rls_policies.sql',
  '003_seed_settings.sql',
  '004_functions.sql',
  '005_notifications_dedup.sql',
  '006_schema_compat.sql',
  '006_production_readiness.sql',
];

let out = `-- AUTO-GENERATED by scripts/build-supabase-migrations.mjs — do not edit by hand.\n`;
out += `-- Run in Supabase SQL Editor (Project → SQL) in order is already preserved.\n\n`;

for (const f of files) {
  const path = join(root, 'sql', f);
  const body = readFileSync(path, 'utf8');
  out += `\n-- ═══════════════════════════════════════════════════════════════\n`;
  out += `-- FILE: sql/${f}\n`;
  out += `-- ═══════════════════════════════════════════════════════════════\n\n`;
  out += body.trimEnd();
  out += '\n\n';
}

const dest = join(root, 'sql', 'ALL_MIGRATIONS_ORDERED.sql');
writeFileSync(dest, out, 'utf8');
console.log(`Wrote ${dest}`);
